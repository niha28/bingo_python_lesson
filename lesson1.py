# print,inputを使うことでpythonに触れる
# 英語のプログラミングに嫌悪感を与えないようにprintの中身は自由にして楽しんでもらってください

# printを使うとコンソールに文字などを表示できる
print("Hello,World")

# ,や+で2つの異なる文字列を合わせて表示できる
print("須貝元","20歳")
print("須貝心"+"10歳")

# 文字列は変数に代入できる
# pythonの場合は変数は"="でかんたんに作成可能
likes = "python,game,Youtube"
print(likes)

# inputを使うと変数に文字列を代入できる
name = input("あなたの名前を教えて下さい\n")
print("18歳です"+name)


# 演習例
print("今から心理テストをします\n正直に答えなくても結構です")
a = input("あなたの名前を教えて下さい")
b = input("あなたの年齢を教えて下さい")
c = input("あなたの性別を教えて下さい")
print("結果")
print("あなたは",a,"で",b,"で",c,"ですね")
